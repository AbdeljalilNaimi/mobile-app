// Reschedule an appointment (provider action)
export const rescheduleAppointment = async (
  id: string,
  newDateTime: string,
  rescheduledBy: string
): Promise<void> => {
  const docRef = doc(db, APPOINTMENTS_COLLECTION, id);
  const docSnap = await getDoc(docRef);
  if (!docSnap.exists()) throw new Error('Appointment not found');
  
  const data = docSnap.data();
  const oldDateTime = data.dateTime?.toDate?.()?.toISOString() || data.dateTime;
  const newDate = new Date(newDateTime);
  
  if (newDate < new Date()) throw new Error('Cannot reschedule to a past date');
  if (newDate.getDay() === 0) throw new Error('Cannot reschedule to Sunday');
  
  await updateDoc(docRef, {
    dateTime: Timestamp.fromDate(newDate),
    status: 'confirmed',
    updatedAt: Timestamp.now(),
    rescheduleHistory: arrayUnion({
      from: oldDateTime,
      to: newDate.toISOString(),
      by: rescheduledBy,
      at: new Date().toISOString(),
    }),
  });
};

// Subscribe to real-time appointment updates for a patient
// Falls back to simpler query if composite index is missing
export const subscribeToPatientAppointments = (
  patientId: string,
  callback: (appointments: Appointment[]) => void,
  onError?: (error: Error) => void
): (() => void) => {
  let unsubscribe: (() => void) | null = null;

  const sortAndDeliver = (snapshot: any) => {
    const appointments = snapshot.docs.map(docToAppointment);
    appointments.sort((a: Appointment, b: Appointment) =>
      new Date(b.dateTime).getTime() - new Date(a.dateTime).getTime()
    );
    callback(appointments);
  };

  const indexedQ = query(
    collection(db, APPOINTMENTS_COLLECTION),
    where('patientId', '==', patientId),
    orderBy('dateTime', 'desc')
  );

  unsubscribe = onSnapshot(indexedQ, sortAndDeliver, (error) => {
    console.warn('Patient indexed query failed, using fallback:', error.message);
    const fallbackQ = query(
      collection(db, APPOINTMENTS_COLLECTION),
      where('patientId', '==', patientId)
    );
    unsubscribe = onSnapshot(fallbackQ, sortAndDeliver, (err) => {
      console.error('Fallback patient appointments error:', err);
      if (onError) onError(err);
      else callback([]);
    });
  });

  return () => {
    if (unsubscribe) unsubscribe();
  };
};

// Subscribe to real-time appointment updates for a provider (by auth UID)
// Falls back to a simpler query (no orderBy) if composite index is missing
export const subscribeToProviderAppointments = (
  providerUserId: string,
  callback: (appointments: Appointment[]) => void,
  onError?: (error: Error) => void
): (() => void) => {
  let unsubscribe: (() => void) | null = null;
  let usingFallback = false;

  const sortAndDeliver = (snapshot: any) => {
    const appointments = snapshot.docs.map(docToAppointment);
    // Sort client-side (desc) to guarantee order even without index
    appointments.sort((a: Appointment, b: Appointment) =>
      new Date(b.dateTime).getTime() - new Date(a.dateTime).getTime()
    );
    callback(appointments);
  };

  const subscribeFallback = () => {
    usingFallback = true;
    console.warn('Using fallback query without orderBy for provider appointments');
    const fallbackQ = query(
      collection(db, APPOINTMENTS_COLLECTION),
      where('providerUserId', '==', providerUserId)
    );
    unsubscribe = onSnapshot(fallbackQ, sortAndDeliver, (error) => {
      console.error('Fallback provider appointments error:', error);
      if (onError) onError(error);
      else callback([]);
    });
  };

  // Try the indexed query first
  const indexedQ = query(
    collection(db, APPOINTMENTS_COLLECTION),
    where('providerUserId', '==', providerUserId),
    orderBy('dateTime', 'desc')
  );

  unsubscribe = onSnapshot(indexedQ, sortAndDeliver, (error) => {
    console.warn('Indexed query failed, trying fallback:', error.message);
    // If index missing (code 9) or permission error, try fallback
    subscribeFallback();
  });

  return () => {
    if (unsubscribe) unsubscribe();
  };
};

// Get upcoming appointments count for a provider (this week) - uses auth UID
export const getUpcomingAppointmentsCount = async (providerUserId: string): Promise<number> => {
  const appointments = await getAppointmentsByProvider(providerUserId);
  const now = new Date();
  const weekFromNow = new Date(now.getTime() + 7 * 24 * 60 * 60 * 1000);
  
  return appointments.filter(apt => {
    const aptDate = new Date(apt.dateTime);
    return aptDate >= now && aptDate <= weekFromNow && apt.status !== 'cancelled';
  }).length;
};

