// Get available slots for a provider on a specific date based on their schedule
export interface AvailabilitySlot {
  time: string;
  status: 'available' | 'booked' | 'past';
}

export const getAvailabilityForDate = async (
  providerId: string,
  date: Date,
  schedule?: Record<string, { open: string; close: string; closed?: boolean } | undefined>
): Promise<AvailabilitySlot[]> => {
  // Default time slots (30-minute intervals from 8am to 6pm)
  const defaultSlots = [
    '08:00', '08:30', '09:00', '09:30', '10:00', '10:30', '11:00', '11:30',
    '12:00', '12:30', '14:00', '14:30', '15:00', '15:30', '16:00', '16:30', 
    '17:00', '17:30'
  ];
  
  // Get booked slots
  const bookedSlots = await getBookedSlotsForDate(providerId, date);
  
  // Get current time for "past" status
  const now = new Date();
  const isToday = date.toDateString() === now.toDateString();
  
  // Generate slots based on provider schedule or defaults
  let availableTimeRange = { open: '08:00', close: '18:00' };
  
  if (schedule) {
    const dayNames = ['dimanche', 'lundi', 'mardi', 'mercredi', 'jeudi', 'vendredi', 'samedi'];
    const daySchedule = schedule[dayNames[date.getDay()]];
    
    if (daySchedule && !daySchedule.closed) {
      availableTimeRange = { open: daySchedule.open, close: daySchedule.close };
    } else if (daySchedule?.closed) {
      return []; // Closed on this day
    }
  }
  
  // Filter slots within provider's working hours
  const slots: AvailabilitySlot[] = defaultSlots
    .filter(slot => {
      return slot >= availableTimeRange.open && slot < availableTimeRange.close;
    })
    .map(time => {
      // Check if slot is in the past
      if (isToday) {
        const [hours, minutes] = time.split(':').map(Number);
        const slotTime = new Date(date);
        slotTime.setHours(hours, minutes, 0, 0);
        
        if (slotTime <= now) {
          return { time, status: 'past' as const };
        }
      }
      
      // Check if slot is booked
      if (bookedSlots.includes(time)) {
        return { time, status: 'booked' as const };
      }
      
      return { time, status: 'available' as const };
    });
  
  return slots;
};

