import { 
  collection, 
  doc, 
  getDocs, 
  getDoc,
  addDoc, 
  updateDoc, 
  query, 
  where, 
  orderBy,
  Timestamp,
  onSnapshot,
  arrayUnion
} from 'firebase/firestore';
import { z } from 'zod';
import { db } from '@/lib/firebase';
import { Appointment } from '@/types/appointments';

const APPOINTMENTS_COLLECTION = 'appointments';

// Input validation schemas
const appointmentInputSchema = z.object({
  providerId: z.string().min(1, 'Provider ID required'),
  providerName: z.string().trim().min(1, 'Provider name required').max(200),
  patientName: z.string().trim().min(2, 'Name must be at least 2 characters').max(100),
  patientPhone: z.string().trim().min(8, 'Phone must be at least 8 characters').max(20),
  patientEmail: z.string().email('Invalid email').max(255).optional().or(z.literal('')),
  patientId: z.string().min(1, 'Patient ID required'),
  dateTime: z.string().refine((val) => !isNaN(Date.parse(val)), 'Invalid date format'),
  notes: z.string().max(500, 'Notes too long').optional(),
  urgency: z.enum(['urgent', 'routine']).optional(),
});

const statusSchema = z.enum(['pending', 'confirmed', 'cancelled', 'completed']);

// Helper to convert Firestore doc to Appointment
const docToAppointment = (docSnap: any): Appointment => {
  const data = docSnap.data();
  return {
    id: docSnap.id,
    providerId: data.providerId,
    providerName: data.providerName,
    patientName: data.patientName,
    patientPhone: data.patientPhone,
    patientEmail: data.patientEmail,
    patientId: data.patientId,
    dateTime: data.dateTime?.toDate?.()?.toISOString() || data.dateTime,
    status: data.status || 'pending',
    urgency: data.urgency || 'routine',
    notes: data.notes,
    rescheduleHistory: data.rescheduleHistory || [],
    createdAt: data.createdAt?.toDate?.()?.toISOString() || data.createdAt,
    updatedAt: data.updatedAt?.toDate?.()?.toISOString() || data.updatedAt
  };
};

// Create a new appointment
export const createAppointment = async (
  appointment: Omit<Appointment, 'id' | 'createdAt' | 'updatedAt'>
): Promise<string> => {
  // Validate input
  const validationResult = appointmentInputSchema.safeParse(appointment);
  if (!validationResult.success) {
    throw new Error(validationResult.error.errors[0]?.message || 'Invalid appointment data');
  }

  const validatedData = validationResult.data;
  const now = Timestamp.now();
  
  // Validate date is not in the past
  const appointmentDate = new Date(validatedData.dateTime);
  if (appointmentDate < new Date()) {
    throw new Error('Cannot book appointments in the past');
  }
  
  // Validate not on Sunday (day 0)
  if (appointmentDate.getDay() === 0) {
    throw new Error('Cannot book appointments on Sundays');
  }
  
  // Extract user ID from provider ID (format: provider_{userId})
  const providerUserId = validatedData.providerId.replace(/^provider_/, '');
  
  // Remove undefined fields — Firestore rejects them
  const docData: Record<string, any> = {
    providerId: validatedData.providerId,
    providerName: validatedData.providerName,
    patientName: validatedData.patientName,
    patientPhone: validatedData.patientPhone,
    patientId: validatedData.patientId,
    urgency: validatedData.urgency || 'routine',
    providerUserId,
    dateTime: Timestamp.fromDate(appointmentDate),
    status: 'pending',
    rescheduleHistory: [],
    createdAt: now,
    updatedAt: now,
  };
  if (validatedData.patientEmail) docData.patientEmail = validatedData.patientEmail;
  if (validatedData.notes) docData.notes = validatedData.notes;

  const docRef = await addDoc(collection(db, APPOINTMENTS_COLLECTION), docData);
  
  // Auto-create first note in subcollection if booking has notes/reason
  if (validatedData.notes) {
    try {
      await addDoc(collection(db, APPOINTMENTS_COLLECTION, docRef.id, 'notes'), {
        senderName: validatedData.patientName,
        senderRole: 'patient',
        senderId: validatedData.patientId,
        content: `Motif: ${validatedData.notes}`,
        isAutoGenerated: true,
        createdAt: Timestamp.now(),
      });
    } catch (e) {
      console.error('Failed to auto-create first note:', e);
    }
  }
  
  return docRef.id;
};

// Get appointments by patient ID
export const getAppointmentsByPatient = async (patientId: string): Promise<Appointment[]> => {
  try {
    const q = query(
      collection(db, APPOINTMENTS_COLLECTION),
      where('patientId', '==', patientId),
      orderBy('dateTime', 'desc')
    );
    
    const snapshot = await getDocs(q);
    return snapshot.docs.map(docToAppointment);
  } catch {
    return [];
  }
};

