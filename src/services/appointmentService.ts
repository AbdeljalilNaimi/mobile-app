// Get booked slots for a specific provider on a specific date
export const getBookedSlotsForDate = async (
  providerId: string, 
  date: Date
): Promise<string[]> => {
  try {
    // Set date range for the entire day
    const startOfDay = new Date(date);
    startOfDay.setHours(0, 0, 0, 0);
    
    const endOfDay = new Date(date);
    endOfDay.setHours(23, 59, 59, 999);
    
    const q = query(
      collection(db, APPOINTMENTS_COLLECTION),
      where('providerId', '==', providerId),
      where('dateTime', '>=', Timestamp.fromDate(startOfDay)),
      where('dateTime', '<=', Timestamp.fromDate(endOfDay))
    );
    
    const snapshot = await getDocs(q);
    
    // Extract booked time slots (only non-cancelled appointments)
    const bookedSlots: string[] = [];
    snapshot.docs.forEach(doc => {
      const data = doc.data();
      if (data.status !== 'cancelled') {
        const dateTime = data.dateTime?.toDate?.() || new Date(data.dateTime);
        const hours = dateTime.getHours().toString().padStart(2, '0');
        const minutes = dateTime.getMinutes().toString().padStart(2, '0');
        bookedSlots.push(`${hours}:${minutes}`);
      }
    });
    
    return bookedSlots;
  } catch (error) {
    console.error('Error fetching booked slots:', error);
    return [];
  }
};

